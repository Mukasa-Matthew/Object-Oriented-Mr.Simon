def area_of_circle(radius):
    return 3.14 * radius * radius
r1 = 5
print("area of circle with radius", r1, "is", area_of_circle(r1))



def area_of_rectangle(length, width):
    return length * width
l1 = 10
w1 = 5
print("area of rectangle with length", l1, "and width", w1, "is", area_of_rectangle(l1, w1))